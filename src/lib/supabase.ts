// 工具函数
export const formatNumber = (num: number, decimals = 2) => {
  if (num == null || isNaN(num)) return '-';
  return num.toLocaleString('zh-CN', {
    minimumFractionDigits: decimals,
    maximumFractionDigits: decimals
  });
};

export const calculateEUI = (totalEnergy: number, buildingArea: number) => {
  if (!buildingArea || buildingArea <= 0) return 0;
  return totalEnergy / buildingArea;
};

export const calculateAnnualTotal = (months: number[]) => {
  return months.reduce((sum, val) => sum + (val || 0), 0);
};

